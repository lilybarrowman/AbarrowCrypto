  private static int[] ROUND_WORD_CONSTANT_ARRAY = new int[] {
    0x8d, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
  };

  private int[] roundKeys; // nK columns 4 rows

  private int numberOfRounds; // number of rounds

  private int[] state;

  public AES(byte[] aesKey) {
    // 128 bit 16 byte 4 word
    // 192 bit 24 byte 6 word
    // 256 bit 32 byte 8 word
    numberOfRounds = getNumberOfRounds(aesKey.length);
    state = new int[AES.STATE_COLUMNS];
    roundKeys = expandRoundKeys(aesKey);
  }
  
  
  private static int getNumberOfRounds(int keyBytes) {
    if (keyBytes == 16) {
      return 10;
    } else if (keyBytes == 24) {
      return 12;
    } else if (keyBytes == 32) {
      return 14;
    } else {
      throw new IllegalArgumentException("The supplied key is not a valid length;");
    }
  }
  
  public static int[] expandRoundKeys(byte[] aesKey) {
    
    int keyBytes = aesKey.length;
    
    int rounds;
    
    if (keyBytes == 16) {
      rounds = 10;
    } else if (keyBytes == 24) {
      rounds = 12;
    } else if (keyBytes == 32) {
      rounds = 14;
    } else {
      throw new IllegalArgumentException("The supplied key is not a valid length;");
    }

    int intsInKey = keyBytes / 4;

    int[] expandedRoundKeys = new int[4 * (rounds + 1)];

    int i;
    for (i = 0; i < intsInKey; i++) {
      expandedRoundKeys[i] = CryptoUtils.intFromBytes(aesKey, i * 4, true);
    }

    while (i < (4 * (rounds + 1))) {
      int temp = expandedRoundKeys[i - 1];
      if (i % intsInKey == 0) {

        
        temp = subWord(rotWord(temp), AES.S_BOX_MAP) ^ AES.ROUND_WORD_CONSTANT_ARRAY[i / intsInKey];
      } else if ((intsInKey > 6) && ((i % intsInKey) == 4)) {
        temp = subWord(temp, AES.S_BOX_MAP);
      }
      expandedRoundKeys[i] = expandedRoundKeys[i - intsInKey] ^ temp;
      i++;
    }
    
    return expandedRoundKeys;
  }

  private void addRoundKey(int offSet) {
    for (int xn = 0; xn < AES.STATE_COLUMNS; xn++) {
      state[xn] ^= roundKeys[offSet + xn];
    }
  }

  private void inverseSubBytes() {
    for (int xn = 0; xn < AES.STATE_COLUMNS; xn++) {
      state[xn] = subWord(state[xn], AES.INVERSE_S_BOX_MAP);
    }
  }

  private void subBytes() {
    for (int xn = 0; xn < AES.STATE_COLUMNS; xn++) {
      state[xn] = subWord(state[xn], AES.S_BOX_MAP);
    }
  }

  private void inverseMixColumns() {
    for (int xn = 0; xn < AES.STATE_COLUMNS; xn++) {
      state[xn] = multiplyInts(0x0b0d090e, state[xn]);
    }
  }

  private void mixColumns() {
    for (int xn = 0; xn < AES.STATE_COLUMNS; xn++) {
      state[xn] = multiplyInts(0x03010102, state[xn]);
    }
  }

  private void inverseShiftRows() {
    int a = state[0];
    int b = state[1];
    int c = state[2];
    int d = state[3];

    state[0] = (b & 0xff000000) + (c & 0xff0000) + (d & 0xff00) + (a & 0xff);
    state[1] = (c & 0xff000000) + (d & 0xff0000) + (a & 0xff00) + (b & 0xff);
    state[2] = (d & 0xff000000) + (a & 0xff0000) + (b & 0xff00) + (c & 0xff);
    state[3] = (a & 0xff000000) + (b & 0xff0000) + (c & 0xff00) + (d & 0xff);
  }

  private void shiftRows() {
    int a = state[0];
    int b = state[1];
    int c = state[2];
    int d = state[3];

    state[0] = (d & 0xff000000) + (c & 0xff0000) + (b & 0xff00) + (a & 0xff);
    state[1] = (a & 0xff000000) + (d & 0xff0000) + (c & 0xff00) + (b & 0xff);
    state[2] = (b & 0xff000000) + (a & 0xff0000) + (d & 0xff00) + (c & 0xff);
    state[3] = (c & 0xff000000) + (b & 0xff0000) + (a & 0xff00) + (d & 0xff);

  }

  public static int subWord(int a, int[] sBox) {
    return (sBox[(a >>> 24) & 0xff] << 24) + (sBox[(a >>> 16) & 0xff] << 16) + (sBox[(a >>> 8) & 0xff] << 8)
        + sBox[a & 0xff];
  }

  public static int rotWord(int a) {
    return ((a & 0xff) << 24) + ((a & 0xff000000) >>> 8) + ((a & 0xff0000) >>> 8) + ((a & 0xff00) >>> 8);
  }

  public static int multiplyInts(int a, int b) {
    int a0 = a & 0xff;
    int a1 = (a >>> 8) & 0xff;
    int a2 = (a >>> 16) & 0xff;
    int a3 = (a >>> 24) & 0xff;

    int b0 = b & 0xff;
    int b1 = (b >>> 8) & 0xff;
    int b2 = (b >>> 16) & 0xff;
    int b3 = (b >>> 24) & 0xff;

    int d0 = (multiplyBytes(a0, b0) ^ multiplyBytes(a3, b1) ^ multiplyBytes(a2, b2) ^ multiplyBytes(a1, b3)) & 0xff;
    int d1 = (multiplyBytes(a1, b0) ^ multiplyBytes(a0, b1) ^ multiplyBytes(a3, b2) ^ multiplyBytes(a2, b3)) & 0xff;
    int d2 = (multiplyBytes(a2, b0) ^ multiplyBytes(a1, b1) ^ multiplyBytes(a0, b2) ^ multiplyBytes(a3, b3)) & 0xff;
    int d3 = (multiplyBytes(a3, b0) ^ multiplyBytes(a2, b1) ^ multiplyBytes(a1, b2) ^ multiplyBytes(a0, b3)) & 0xff;

    return (d3 << 24) + (d2 << 16) + (d1 << 8) + d0;
  }

  public static int multiplyBytes(int a, int b) {
    int out = 0;
    for (int n = 7; n >= 0; n--) {
      out ^= (b & (1 << n)) * a;
    }

    // this line might not be needed
    out = out & 0x7fff;

    // polynomial long division in this absurd world
    int count = 0;
    while (out >= 256 && count < 100) {
      int c = out;
      int j = -1;
      while (c > 0) {
        c = c >>> 1;
        j++;
      }
      // 1 << j is the most significant bit
      out ^= (1 << j) + Math.max(1 << (j - 4), 0) + Math.max(1 << (j - 5), 0) + Math.max(1 << (j - 7), 0)
          + Math.max(1 << (j - 8), 0);
      count++;
    }
    return out;
  }

  @Override
  public int getBlockBytes() {
    return 16;
  }

  private void copyStateFromByteArray(byte[] input, int srcPos) {
    for (int xn = 0; xn < AES.STATE_COLUMNS; xn++) {
      state[xn] = CryptoUtils.intFromBytes(input, srcPos + 4 * xn, true);
    }
  }

  private void copyStateToByteArray(byte[] output, int destPos) {
    for (int xn = 0; xn < AES.STATE_COLUMNS; xn++) {
      CryptoUtils.intToBytes(state[xn], output, destPos + 4 * xn, true);
      // clean up the state
      state[xn] = 0;
    }
  }
  
